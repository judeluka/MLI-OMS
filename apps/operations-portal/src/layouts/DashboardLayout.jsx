function DashboardLayout({ onLogout }) {
  const navigate = useNavigate();
  const location = useLocation();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  const handleLogoutClick = () => {
    onLogout();
    navigate('/login');
  };

  // Navigation items following the booking-portal pattern
  const navigation = [
    { name: 'Overview', href: '/dashboard', icon: HomeIcon },
    { name: 'Groups', href: '/dashboard/groups', icon: UserGroupIcon },
    { name: 'Participants', href: '/dashboard/participants', icon: UsersIcon },
    { name: 'Centres', href: '/dashboard/centres', icon: MapPinIcon },
    { name: 'Staff', href: '/dashboard/staff', icon: UsersIcon },
    { name: 'Agencies', href: '/dashboard/agencies', icon: BuildingOfficeIcon },
    { name: 'Programmes', href: '/dashboard/programmes', icon: AcademicCapIcon },
    { name: 'Rota', href: '/dashboard/rota', icon: CalendarDaysIcon },
    { name: 'Activities', href: '/dashboard/activities', icon: SparklesIcon },
    { name: 'Flights', href: '/dashboard/flights', icon: PaperAirplaneIcon },
    { name: 'Transfers', href: '/dashboard/transfers', icon: ArrowPathIcon },
    { name: 'Sales Grid', href: '/sales-grid', icon: TableCellsIcon },
    { name: 'Import', href: '/dashboard/import', icon: DocumentTextIcon },
    { name: 'Users', href: '/dashboard/users', icon: UsersIcon },
    { name: 'Settings', href: '/dashboard/settings', icon: SettingsIcon },
    { name: 'Requests', href: '/dashboard/requests', icon: DocumentCheckIcon },
  ];

  const storedUser = localStorage.getItem('user');
  let userEmail = 'Operations Team';
  if (storedUser) {
    try {
      const userData = JSON.parse(storedUser);
      userEmail = userData.email || 'Operations Team';
    } catch (error) {
      console.error("Error parsing user data from localStorage:", error);
    }
  }

  const isActiveRoute = (href) => {
    if (href === '/dashboard') {
      return location.pathname === href;
    }
    return location.pathname.startsWith(href);
  };

  return (
    <div className="min-h-screen bg-gray-50 relative overflow-x-hidden">
      {/* Mobile sidebar overlay */}
      <div className={`fixed inset-0 z-50 lg:hidden ${sidebarOpen ? 'block' : 'hidden'}`}>
        <div className="fixed inset-0 bg-gray-600 bg-opacity-75" onClick={() => setSidebarOpen(false)} />
        
        {/* Mobile sidebar */}
        <div className="fixed inset-y-0 left-0 flex w-64 flex-col bg-white shadow-xl">
          {/* Mobile header */}
          <div className="flex items-center justify-between px-4 py-6 border-b border-gray-200">
            <div className="text-xl font-bold text-blue-600">Operations Portal</div>
            <button
              onClick={() => setSidebarOpen(false)}
              className="text-gray-400 hover:text-gray-600 transition-colors"
            >
              <XMarkIcon className="h-6 w-6" />
            </button>
          </div>

          {/* Mobile navigation */}
          <nav className="flex-1 px-4 py-6 space-y-2">
            {navigation.map((item) => {
              const isActive = isActiveRoute(item.href);
              return (
                <Link
                  key={item.name}
                  to={item.href}
                  className={`flex items-center px-3 py-2 text-sm font-medium rounded-lg transition-colors ${
                    isActive
                      ? 'bg-blue-100 text-blue-700'
                      : 'text-gray-700 hover:bg-gray-100'
                  }`}
                  onClick={() => setSidebarOpen(false)}
                >
                  <item.icon className="mr-3 h-5 w-5" />
                  {item.name}
                </Link>
              );
            })}
          </nav>

          {/* Mobile user section */}
          <div className="px-4 py-4 border-t border-gray-200">
            <div className="flex items-center px-3 py-2 text-sm">
              <div className="flex-shrink-0">
                <div className="w-8 h-8 bg-blue-100 rounded-full flex items-center justify-center">
                  <span className="text-blue-600 font-medium">
                    {userEmail.charAt(0).toUpperCase()}
                  </span>
                </div>
              </div>
              <div className="ml-3 min-w-0 flex-1">
                <p className="text-gray-700 font-medium truncate">
                  {userEmail}
                </p>
                <p className="text-gray-500 text-xs truncate">
                  MLI International Schools
                </p>
              </div>
            </div>
            <button
              onClick={handleLogoutClick}
              className="w-full mt-2 flex items-center px-3 py-2 text-sm font-medium text-gray-700 hover:bg-gray-100 rounded-lg transition-colors"
            >
              <ArrowRightOnRectangleIcon className="mr-3 h-5 w-5" />
              Sign Out
            </button>
          </div>
        </div>
      </div>

      {/* Desktop sidebar */}
      <div className="hidden lg:fixed lg:inset-y-0 lg:z-40 lg:flex lg:w-64 lg:flex-col">
        <div className="flex flex-col flex-grow bg-white border-r border-gray-200 shadow-sm relative z-10">
          {/* Desktop header */}
          <div className="px-6 py-6 border-b border-gray-200">
            <div className="text-2xl font-bold text-blue-600">Operations Portal</div>
            <div className="mt-2 text-sm text-gray-600">MLI International Schools</div>
          </div>
          
          {/* Desktop navigation */}
          <nav className="flex-1 px-4 py-6 space-y-2">
            {navigation.map((item) => {
              const isActive = isActiveRoute(item.href);
              return (
                <Link
                  key={item.name}
                  to={item.href}
                  className={`flex items-center px-3 py-2 text-sm font-medium rounded-lg transition-colors ${
                    isActive
                      ? 'bg-blue-100 text-blue-700'
                      : 'text-gray-700 hover:bg-gray-100'
                  }`}
                >
                  <item.icon className="mr-3 h-5 w-5" />
                  {item.name}
                </Link>
              );
            })}
          </nav>

          {/* Desktop user section */}
          <div className="px-4 py-4 border-t border-gray-200">
            <div className="flex items-center px-3 py-2 text-sm">
              <div className="flex-shrink-0">
                <div className="w-8 h-8 bg-blue-100 rounded-full flex items-center justify-center">
                  <span className="text-blue-600 font-medium">
                    {userEmail.charAt(0).toUpperCase()}
                  </span>
                </div>
              </div>
              <div className="ml-3 min-w-0 flex-1">
                <p className="text-gray-700 font-medium truncate">
                  {userEmail}
                </p>
                <p className="text-gray-500 text-xs truncate">
                  MLI International Schools
                </p>
              </div>
            </div>
            <button
              onClick={handleLogoutClick}
              className="w-full mt-2 flex items-center px-3 py-2 text-sm font-medium text-gray-700 hover:bg-gray-100 rounded-lg transition-colors"
            >
              <ArrowRightOnRectangleIcon className="mr-3 h-5 w-5" />
              Sign Out
            </button>
          </div>
        </div>
      </div>

      {/* Main content area */}
      <div className="lg:pl-64 main-content-area">
        {/* Top header */}
        <header className="bg-white shadow-sm border-b border-gray-200 sticky top-0 z-30">
          <div className="px-4 sm:px-6 lg:px-8">
            <div className="flex items-center justify-between h-16">
              {/* Mobile menu button */}
              <button
                onClick={() => setSidebarOpen(true)}
                className="lg:hidden text-gray-500 hover:text-gray-700 transition-colors"
              >
                <Bars3Icon className="h-6 w-6" />
              </button>

              {/* Page title area - filled by individual pages */}
              <div className="flex-1" />
            </div>
          </div>
        </header>

        {/* Page content */}
        <main className="min-h-screen bg-gray-50 w-full">
          <div className="w-full max-w-none">
            <Outlet />
          </div>
        </main>
      </div>
    </div>
  );
}

export default DashboardLayout;